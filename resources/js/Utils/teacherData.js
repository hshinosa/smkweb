// Helper functions untuk filter dan search
export const filterTeachersByDepartment = (department) => {
    if (department === "Semua") {
        return mockTeacherData;
    }
    return mockTeacherData.filter(teacher => teacher.department === department);
};

export const filterTeachersByStatus = (status) => {
    if (status === "Semua") {
        return mockTeacherData;
    }
    return mockTeacherData.filter(teacher => teacher.status === status);
};

export const filterTeachersByType = (type) => {
    if (type === "Semua") {
        return mockTeacherData;
    }
    return mockTeacherData.filter(teacher => teacher.type === type);
};

export const searchTeachers = (query) => {
    const lowercaseQuery = query.toLowerCase();
    return mockTeacherData.filter(teacher => 
        teacher.name.toLowerCase().includes(lowercaseQuery) ||
        teacher.position.toLowerCase().includes(lowercaseQuery) ||
        (teacher.subject && teacher.subject.toLowerCase().includes(lowercaseQuery)) ||
        teacher.department.toLowerCase().includes(lowercaseQuery)
    );
};

